	<h2>E210, mini-PC đầu tiên không dùng quạt của ASUS</h2>
	<p>(PCWorldVN) Theo Slashgear, mẫu PC mới ASUS E210 có sức mạnh tương đương một máy chủ nhỏ trong gia đình, nhưng ngoại hình lại rất gọn nhẹ.</p>
	<br>
	<p>Thiết kế mini-PC không dùng quạt tản nhiệt dường như đang là một cuộc chạy đua mới của các hãng máy tính trên thế giới trong thời gian gần đây .</p>
	<br>
	<p>Mới hồi đầu năm 2014, ASUS đã cố gắng tham gia cuộc đua này, nhưng kết quả có vẻ không mấy thành công khi từng trình làng chiếc mini-PC Eee Box 1037 vẫn trang bị quạt tản nhiệt.</p>
	<img src="images/new1.jpg" style="margin:0 auto;">
	<br>
	<p>Tuy nhiên, sau một thời gian khá dài, hôm nay nhà sản xuất máy tính đến từ Đài Loan này đã thành công và chính thức trình làng mẫu mini-PC mới tên gọi E210 đầu tiên không trang bị bất kỳ quạt tản nhiệt nào bên trong.</p>
	<br>
	<p>Đại diện ASUS cho biết, thiết kế mới của E210 không chỉ thu gọn đáng kể kích thước của bộ mini-PC mới giúp tiết kiệm không gian sử dụng mà còn mang lại cho người dùng một môi trường làm việc thực sự yên tĩnh.</p>
	<br>
	<p>ASUS E210 có thể treo tường, đặt trên bàn hoặc giấu gọn ở mặt sau các loại màn hình một cách dễ dàng. Hãng còn bán kèm sản phẩm một bộ chân đế xoay đa dụng cho phép người dùng tùy biến cách sử dụng theo ý mình.</p>
	<br>
	<p>Về cấu hình, ASUS E210 trang bị bộ xử lý lõi kép Intel Celeron N2807 tốc độ 2.16 GHz, RAM 2 GB (có thể mở rộng đến 4 GB khi cần thiết), ổ cứng SSD 128GB, đồ họa Intel HD GPU, đầu đọc thẻ 4-in-1, cổng giao tiếp USB 3.0, USB 2.0, HDMI, Ethernet, Wi-Fi 802.11b/g/n.</p>
	<br>
	<p>Theo ASUS, bộ máy tính mini-PC E210 này không chỉ hỗ trợ Windows Embedded 8 và Windows Embedded mà còn “chơi” với cả DOS.</p>
	<br>
	<p>ASUS E210 có kích thước 218x152x28 mm, cân nặng khoảng 680 gram.</p>
